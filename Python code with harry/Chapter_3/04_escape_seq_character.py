a="Aditya is a good boy\nbut not a bad\t boy"  #\n is new line  \t gives tab space
b="Aditya is a good boy\nbut not a bad \"boy\" "  # \"boy\" to add quotes

print(a)
print(b)

# 1. \n → New line
print("Hello\nWorld")
# Output:
# Hello
# World

# 2. \t → Tab space
print("Hello\tWorld")
# Output: Hello   World

# 3. \\ → Backslash
print("C:\\Users\\Aditya")
# Output: C:\Users\Aditya

# 4. \' → Single quote
print('It\'s a sunny day')
# Output: It's a sunny day

# 5. \" → Double quote
print("He said, \"Python is awesome!\"")
# Output: He said, "Python is awesome!"
